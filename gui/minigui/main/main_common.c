#define MSG_SIZE (sizeof(msg_code_info) / sizeof(msg_info_t))

char *trans_msg_code(int message) {
	char *code = NULL;
	int i;

	for(i = 0; i < MSG_SIZE; i++) {
		if(msg_code_info[i].code == message) {
			code = msg_code_info[i].des;
			break;
		}
	}

	return code;
}

